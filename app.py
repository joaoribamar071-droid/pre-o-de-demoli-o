def extract_text_from_pdf(file):
    text = ""
    with fitz.open(stream=file.read(), filetype="pdf") as doc:
        for page in doc:
            text += page.get_text()
    return text

def extract_text_from_table(file, ext):
    if ext == "csv":
        df = pd.read_csv(file)
    else:
        df = pd.read_excel(file)
    return df

